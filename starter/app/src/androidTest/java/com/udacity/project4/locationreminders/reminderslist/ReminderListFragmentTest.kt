@RunWith(AndroidJUnit4::class)
@ExperimentalCoroutinesApi
//UI Testing
@MediumTest
class ReminderListFragmentTest : KoinTest {

    private lateinit var repository: ReminderDataSource
    private lateinit var appContext: Application

    private val navController: NavController = mock(NavController::class.java)

    @get:Rule
    var instantTaskExecutorRule = InstantTaskExecutorRule()

    @ExperimentalCoroutinesApi
    @get:Rule
    var mainCoroutineRule = MainCoroutineRule()

    @Before
    fun init() {
        stopKoin()//stop the original app koin
        appContext = getApplicationContext()
        val myModule = module {
            viewModel {
                RemindersListViewModel(
                    appContext,
                    get() as ReminderDataSource
                )
            }
            single { RemindersLocalRepository(get()) as ReminderDataSource }
            single { LocalDB.createRemindersDao(appContext) }
        }
        //declare a new koin module
        startKoin {
            androidContext(appContext)
            modules(listOf(myModule))
        }
        //Get our real repository
        repository = get()

        //clear the data to start fresh
        runBlocking {
            repository.deleteAllReminders()
        }
    }

    // test the navigation of the fragments.
    @Test
    fun clickOnFab_navigatesToSaveReminderFragment(){
        // GIVEN fragment launched and navigation setup
       val scenario =
            launchFragmentInContainer<ReminderListFragment>(Bundle(), R.style.AppTheme)
        scenario.onFragment {
            Navigation.setViewNavController(it.view!!, navController)
        }

        // WHEN clicking on the fab
        onView(withId(R.id.addReminderFAB)).perform(click())

        // THEN navigate to SaveReminderFragment
        verify(navController).navigate(ReminderListFragmentDirections.toSaveReminder())
    }

    // test the displayed data on the UI.
    @Test
    fun remindersList_displayListUI(): Unit = runBlocking {
        // GIVEN saved reminder in repository
        val reminder = ReminderDTO("Reminder title", "description 1", "location 1", 15.0, 15.0)
        repository.saveReminder(reminder)

        // when fragment launched
        launchFragmentInContainer<ReminderListFragment>(Bundle(), R.style.AppTheme)

        // THEN the reminder is displayed
        onView(withId(R.id.noDataTextView)).check(matches(not(isDisplayed())))
    }
}